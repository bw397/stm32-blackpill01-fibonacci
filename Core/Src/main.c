/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
    /* Infinite loop */
    HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);

    for(;;) {
        if (cur_value > 9999) {
            // reset
            prev_value = 1;
            cur_value = 0;
        }
        LED_Show(cur_value);
        osDelay(1);  // required to cede control so that other tasks may run (2 works)
    }
  /* USER CODE END 5 */
}

